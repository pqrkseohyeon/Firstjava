package day02;

public class Exam07 {
	public static void main(String[] args) {
		for(int i=1;i<6;i++) {
			System.out.print(i+"\t");
		}
		System.out.println();
		System.out.println("----");
		//1부터 5까지 출력 while
		int n=1;
		while(n<6) {
			System.out.print(n+"\t");
			n++;
		}
		System.out.println("\n-----");
		//1부터 5까지 출력 do~while : 최소한 한번은 수행
		int m=1;
		do {
			System.out.print(m+"\t");
			m++;
		}while(m<6);
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
